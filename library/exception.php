    /**
     * Exception constructor
     * @param message string Message to record for the exception
     * @param [code] int Code for the exception (default 500)
     * @param [previous] Exception Previous exception if nested
     */
    public function __construct($message, $code = 500, Exception $previous = null)
    {
        parent::__construct($message, $code, $previous);
    }

    /**
     * Get the HTTP description for the exception's code
     * @return string Description of the corresponding HTTP code
     */
    public function getCodeDesc()
    {
        return isset(self::$http_codes[$this->getCode()]) ? self::$http_codes[$this->getCode()] : '';
    }
}

